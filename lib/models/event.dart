class Event {
  final String title;
  final String subtitle;
  final String date;
  final String location;
  final String imageUrl;
  final String description;
  final double price;

  Event({
    required this.title,
    required this.subtitle,
    required this.date,
    required this.location,
    required this.imageUrl,
    required this.description,
    required this.price,
  });
}
